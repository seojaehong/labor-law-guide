import os, json, urllib.request, time
from pathlib import Path

def get_env():
    env_path = Path(__file__).parent / '.env'
    for line in env_path.read_text().split('\n'):
        if '=' in line and not line.startswith('#'):
            k, v = line.strip().split('=', 1)
            os.environ.setdefault(k, v)
    return os.environ['SUPABASE_URL'], os.environ['SUPABASE_SERVICE_KEY']

def upload(article):
    url, key = get_env()
    data = json.dumps(article, ensure_ascii=False).encode()
    headers = {
        'apikey': key,
        'Authorization': f'Bearer {key}',
        'Content-Type': 'application/json',
        'Prefer': 'return=representation',
    }
    req = urllib.request.Request(f"{url}/rest/v1/blog_articles", data=data, headers=headers)
    with urllib.request.urlopen(req) as resp:
        result = json.loads(resp.read())
    print(f"✅ 업로드: {article['slug']} | {article['title']}")
    return result

